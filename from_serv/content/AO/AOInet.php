            <h3>Бронирование услуги</h3>
            <p><strong>Бронирование услуги</strong> – это способ резервирования услуги Интернет при ее временной невостребованности (например, отъезд):</p>
            <ul>
                <li>начисляется только абонентская плата за аренду порта на оборудовании;</li>
                <li>не начисляется абонентская плата за пользование услугой Интернет.</li>
            </ul>
            <p>Услуга бронирование выполняется путем смены абонентом текущего тарифного плана на <a href="http://192.168.78.33/?page=tariffs_inet_fl#TP_reserv">ТП «Резерв»</a>.
                Эту процедуру абонент может выполнить самостоятельно в  <a href="http://192.168.78.33/?page=gen#dyniptarifch">личном кабинете</a>
                по телефонному звонку в абонентский отдел (после сверки паспортных данных смена производится через личный кабинет абонента), либо придя в Абонентский Отдел.</p>
            <ol><span style="text-decoration:underline; ">В случае выполнения процедуры через АО необходимо:</span>
                <li>Явиться в Абонентский Отдел</li>
                    <li>
                        <ul>При себе иметь:
                            <li>паспорт, для лица, на чье имя заключен договор, </li>
                            <li>паспорт и <a href="#AO_Inet_exec_auth"><a href="#AO_Inet_exec_auth">доверенность, оформленная в Абонентском Отделе</a>, для представителя лица, на чье имя заключен договор.</li>
                            <li>паспорт и нотариальная доверенность – для представителя лица, на чье имя заключен договор.</li>
                        </ul>
                    </li>
                    <li>Заполнить заявление на смену тарифного плана (приостановление услуги выполняется путем перехода абонента на <a href="http://192.168.78.33/?page=tarifs#TP_reserv">тарифный план «Резерв» </a>. </li>
                    <br>
                <ul>Условия предоставления услуги бронирования:
                    <li>услуга бронирования активируется с даты обращения абонента,
                    <li>дата активации услуги бронирования в день обращения абонента,
                    <li>максимальный срок бронирования –  неопределенный срок.
                </ul>
                <li>Если подключен ТП линейки Феникс и на л/с нет денежных средств, превышающих размер абонентской платы, на бронирование не ставим, так как начисление абонентской платы не будет производиться.</li>
            </ol>
  			
