            <h3>Переоформление услуги на другое ФЛ</h3>
<div class="alert alert-info">Вопросами переоформления услуги на другое ФЛ занимается <a href="#ModAO" data-toggle="modal"> Абонентский Отдел</a>.</div>

<ol>
    <span style="text-decoration:underline; ">Необходимые действия:</span>
        <li>
            <ul>Явиться в Абонентский Отдел следующим лицам:
                <li>лицу, на которое оформлен договор (или его представителю по доверенности), либо предлагаем оформить заявление (ссылка на бланк заявления) и отправить менеджеру Абонентского отдела на электронную почту (уточняем контактный телефон).</li>
                <li>лицу, на которое переоформляется договор</>
            </ul>
    <li>
        <ul>При себе иметь:
            <li>паспорт, для лица, на чье имя заключен договор, </li>
            <li>паспорт и доверенность, <a href="#AO_tv_exec_auth">оформленная в Абонентском Отделе</a>, для представителя лица, на чье имя заключен договор.</li>
            <li>паспорт и нотариальная доверенность – для представителя лица, на чье имя заключен договор и заявление от нынешнего получателя услуги о его согласии на процедуру переоформления</li>
        </ul>
    </li>
<li>Оформить заявление от нынешнего получателя услуги о его согласии на процедуру переоформления (либо получить заявление по электронной почте/факсу)</li>
<li>Заполнить заявление на переоформление услуги на другое ФЛ</li>
</ol>

<p>При отсутствии возможности присутствия лица, на чье имя заключен договор, можно произвести переоформление услуги при предоставлении документа, подтверждающего право собственности, свидетельства о смерти.</p>


