<h3>Бронирование услуги</h3> 
<p><b>Бронирование услуги</b> – это способ резервирования услуги «Телевидение» при ее временной невостребованности (например, отъезд):
    <ul>
        <li><b><i>начисляется только абонентская плата за аренду линии</i></b></li>
        <li><b><i>не начисляется абонентская плата за пользование услугой Телевидение.</i></b></li>
    </ul>

<p>Услуга бронирование выполняется путем смены абонентом текущего тарифного плана на ТП <a href="http://192.168.78.33/?page=tariffs_tv_fl">«Бронирование»</a> </p>
<p>Эту процедуру абонент может выполнить в <a href="#ModAO" data-toggle="modal">Абонентском Отделе</a>.</p>

<ol>
    <span style="text-decoration:underline; ">Необходимые действия:</span>
        <li>Предложить Абоненту возможность отправки заявления на электронную почту.
            Необходимо взять у Абонента л/с, адрес электронной почты, контактный телефон,
            уточнить дату начала бронирования и срок. Передать данные в Абонентский отдел.
            После чего Абонентский отдел свяжется с абонентом  и подготовит все необходимые документы для отправки (заявление).
        </li>
</ol>
<br>
<ol><span style="text-decoration:underline; ">Либо</span>
    <li>Явиться в Абонентский Отдел</li>
    <li>
        <ul>При себе иметь:
            <li>паспорт, для лица, на чье имя заключен договор, </li>
            <li>паспорт, <a href="#AO_tv_exec_auth"> на чье имя</a> заключен договор.</li>
            <li>паспорт и нотариальная доверенность – для представителя лица, на чье имя заключен договор.</li>
        </ul>
    <li>Заполнить заявление на смену тарифного плана
        (приостановление услуги выполняется путем перехода абонента на тарифный план <a href="http://192.168.78.33/?page=tariffs_tv_fl">«Бронирование».</a>
        <ul><strong class="text-warning">Условия предоставления услуги бронирования:</strong>
            <li>услуга бронирования активируется с 1-го числа следующего месяца</li>
            <li>допускается корректировка даты активации услуги бронирования (например, с середины месяца),
                но при этом длительность бронирования должна быть не менее 1 календарного месяца,</li>
            <li>максимальный срок бронирования – 6 мес.</li>
        </ul>
    </li>
    <li>Подписать дополнительное соглашение о смене тарифного плана.</li>
</ol>
