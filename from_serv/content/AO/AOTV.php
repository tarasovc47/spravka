            <h3>Расторжение договора</h3>
<div class="alert alert-info">Вопросами расторжения договоров занимается <a href="#ModAO" data-toggle="modal"> Абонентский Отдел</a>.</div>
<ol>
    <span style="text-decoration:underline; ">Необходимые действия:</span>
    <li>Предложить Абоненту возможность отправки заявления на электронную почту.
        Необходимо взять у Абонента л/с, адрес электронной почты, контактный телефон,
        уточнить дату расторжения договора и причину (смена оператора, нет необходимости,
        переезд вне зоны РК, если же адрес есть в зоне оказания услуг РК,
        предлагаем перенос услуги: смотрим пункт «Перенос услуги»). Передать данные в
        Абонентский отдел. После чего Абонентский отдел свяжется с абонентом для отправки
        и подписания всех необходимых документов (заявление, акт).</li>
</ol>
<ol>
    <span style="text-decoration:underline; ">Либо:</span>
    <li>Явиться в Абонентский Отдел</li>
    <li>
        <ul>При себе иметь:
            <li>паспорт, для лица, на чье имя заключен договор, </li>
            <li>паспорт и доверенность, <a href="#AO_tv_exec_auth">оформленная в Абонентском Отделе</a>, для представителя лица, на чье имя заключен договор.</li>
            <li>паспорт и нотариальная доверенность – для представителя лица, на чье имя заключен договор.</li>
        </ul>
        <li>Заполнить заявление на расторжение договора.</li>
        <li>Ознакомиться с актом сверки платежей, произвести итоговые расчеты.</li>
</ol>
            <br>
<p>Так же Абонент может направить заполненное заявление факсом, почтой или эл. почтой в <b><a href="#ModSecr" role="button" data-toggle="modal" >приемную</a></b>.
    После получения заявления от абонента менеджер Абонентского отдела свяжется с абонентом для уточнения информации и
    подписи необходимых дополнительных документов.</p>
