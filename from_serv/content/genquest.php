				<li>Выбрать «Доверительный доступ», </li> 
				<li>Установить требуемое абоненту количество дней (от 1-го до 5-ти), </li> 
				<li>Принять условия оказания услуги.</li>
			</ol>
			<p>До истечения срока доверительного доступа абоненту необходимо внести на свой лицевой счет денежную сумму в размере абонентской платы по выбранному тарифному плану.
