		<p><strong>Доверительный доступ</strong> – это возможность получения услуги без внесения денежных средств на срок от 1-го до 5-ти дней. 
			<ol>Для того, чтобы воспользоваться опцией «Доверительный доступ», необходимо:
				<li>Перейти на сайт «Русской компании» (t72.ru),</li> 
				<li>Выбрать вкладку «Личный кабинет», </li> 
				<li>Осуществить <a href="#lcab">вход в систему</a>, введя номер лицевого счета либо логин для доступа в Интернет,</li>  
