                                </a>
                            </td>
                        </tr>
                </table> 
<br>
				<P >
				Как видно из данного примера, у абонента
				отрицательный баланс, и он отключен по
				отсутствию предоплаты. В данном случае
				абоненту необходимо погасить задолженность
				и оплатить сумму, равную стоимости
				тарифного плана.</P>

				<P>
				Если же у абонента положительный баланс, но
				он все так же отключен по причине
				отсутствия предоплаты, то ему необходимо
				доплатить сумму, равную стоимости
				тарифного плана.</P>

