					Если у абонента при подключении к сети Интернет браузер открывает страницу ссообщением об отключении за неуплату, то первым делом необходимо проверить наличие необходимой суммы на балансе и не отключен ли он по отсутствию предоплаты.</P>
<br>
                <table align="center">
                        <tr>
                            <td>
                                <a class="fancybox-effects-a"  href="img/TehDif_inet/m4cf1ee8b.jpg" title="Рисунок 1. Недостатка баланса">
