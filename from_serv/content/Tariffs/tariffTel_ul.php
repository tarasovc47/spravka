<h3>II. SIP-телефония (Русфон)</h3>

<p><b>SIP</b>— это передача голосовой информации с использованием в качестве среды передачи IP-сети (локальные и глобальные (Интернет)).</p>
<p>Система SIP-телефонии построена на стандартной для Интернета пакетной системе: голос преобразуется в файлы, которые группируются в пакеты и пересылаются по адресу. Достигнув пункта назначения, они декодируются обратно. 
</p>
<p>SIP-связь обеспечивают SIP-телефоны, которые бывают стационарными (похожими на обычные), софтфоны (программы, устанавливаемые на ПК) или мобильные трубки. Если обычный телефон включается в телефонную розетку, то SIP-телефон точно так же подключается к Интернету. Пользователь получает специальный SIP-адрес, похожий на адрес электронной почты, и может работать с его помощью в любой точке мира, где есть доступ в Сеть. SIP-провайдер обеспечивает регистрацию пользователей, исходящие звонки с SIP-телефонов на стационарные аппараты, а также входящие звонки с обычных телефонов на SIP-устройства. Таким образом, абоненту SIP, где бы он ни находился, можно позвонить не только на SIP-адрес, но и на обычный городской номер (например, тюменский).</p>
<p> <b>Преимущества </b><b>SIP-телефонии:</b></p>
<ul>
	<li>нет	привязки к географическому местоположению бизнеса;
	<li>возможность	использования местного телефонного номера в любом месте, без подключения роуминга или использования МГ/МН связи;
	<li>бесплатное подключение 5-ти одновременно входящих/исходящих линий связи.
</ul>
