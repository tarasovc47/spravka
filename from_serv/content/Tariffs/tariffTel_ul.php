«Виртуальная АТС». 
</p>
<p>
Внутренний
номер – короткая последовательность
цифр, присвоенная пользователю телефонной
сети.</p>
<p>
Выделение
внутренней нумерации позволяет
организовать телефонную связь между
большим количеством пользователей при
ограниченном количестве подключенных
номеров (т.е. имея договор лишь на один
городской номер внутри сети можно
пользоваться телефонами нескольким
сотрудникам, ограничение – невозможно
одновременно осуществлять исходящие
звонки).</p>
