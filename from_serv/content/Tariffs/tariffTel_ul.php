<h3>III. Связь по потоку Е1</h3>

<p><b>Цифровой поток Е1</b> – это стандарт передачи цифровой информации, представляет собой уплотненный канал передачи данных и складывается из 32 мильтиплексируемых каналов (30 B-каналов каждый по 64 кбит/сек для голоса или данных и 2 канала для сигнализации) Общая пропускная способность E1 = 2048 Кбит/c.</p>

<p> Цифровой поток E1 применяется для организации качественных голосовых каналов в многоканальной телефонной связи. </p>

<p><b>Подключение по потоку Е1 обеспечивает ряд преимуществ:</b></p>
<ul>
	<li>высочайшее качество связи и хорошая помехоустойчивость, которые достигаются за счет применения
	цифрового кодирования сигнала;</p>
    <li>организация единого многоканального городского телефона с возможностью распределения звонков внутри компании. В одном потоке может быть до 200 городских номеров;</p>
    <li>используя один цифровой поток, к офисной АТС можно подключить 30 разговорных каналов. Это значит, что 30 сотрудников смогут разговаривать по телефону одновременно (разговоры могут быть как входящими, так и исходящими). 
</ul>
