<h3>I. Стационарная (фиксированная) телефонная связь</h3>
<p>
<b>Услуга стационарной (фиксированной) телефонной связи</b>
— это современная телекоммуникационная услуга, отличающаяся высокой надежностью и позволяющая абонентам общаться на расстоянии, используя для связи телефон с городским номером. 
</p>
<a name="phone_services"></a>
<p>При подключении, абоненту предоставляется доступ к услугам:</p>
<ul>
	<li>Входящей местной телефонной связи;</li>
	<li>Исходящей местной телефонной связи;</li>
	<li>Исходящей телефонной связи на МГ и МН (междугородние и международные) направления;</li>
	<li>Исходящей связи на федеральные сотовые номера;</li>
    <li>Прием / передача факсимильных сообщений;</li>
	<li>Автоматический определитель номера (дополнительная опция, предоставляется при наличии технической возможности). </li>
	</p>
</ul>

<p><b>Преимущества фиксированной телефонной связи:</b></p>
<ul>
	<li>высокая	надежность связи (надежность - 99,999%, или	5 минут отказа в год);
	<li>независимость от электроэнергии (сети 220В);
	<li>простота использования.
</ul>
