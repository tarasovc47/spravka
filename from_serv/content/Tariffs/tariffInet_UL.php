                <P>Предоставление доступа к услугам связи по технологии
                    PON - это технология построения широкополосных
                    сетей мультисервисного доступа, которая
                    позволяет предоставить по одному
                    оптическому волокну услуги передачи
                    данных (Интернет), телефонии, кабельного
                    телевидения с гарантированным качеством
                    обслуживания.
                </P>

                <P>Технология PON предусматривает прокладку оптоволоконного
                    кабеля непосредственно в дом абонента,
                    тем самым гарантируя высокое качество
                    предоставляемых услуг.
                </P>

                <P>Для организации доступа абоненту
                    устанавливается активное оборудование-
                    ONU, благодаря которому подключение всех
                    услуг происходит удаленно и в одном
                    устройстве. Оборудование бывает разных                     типов, может иметь встроенный Wi-Fi роутер, что
                    позволяет избежать установки дополнительных
                    устройств и обеспечить беспроводной
                    доступ.
                </P>
