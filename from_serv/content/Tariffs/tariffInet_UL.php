			<div class="well wellwhite">
                <H4><font color="dark blue">Ethernet</font></H4>
                    <P>По волоконно-оптическому кабелю сигнал
                        поступает в каждый дом, где устанавливается
                        медиа-конвертор, который преобразует
                        световой (оптический) сигнал в электрический
                        сигнал и подключается в коммутатор.
                        Канал – симметричный в обе стороны,
                        исходящая скорость равна входящей.
                    </P>
                    <P>Электрический сигнал от коммутатора поступает в квартиру абонента по локальной сети (кабель - «Витая пара»). Подключение компьютеров к сети Интернет осуществляется по протоколу PPPoE (PPP over Ethernet Protocol) через сетевую карту, установленную в компьютер. Скорость подключения к локальной сети осуществляется на скорости до 100 Мбит/с.
                    </P>
