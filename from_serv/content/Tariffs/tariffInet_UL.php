			<div class="well wellwhite">
				<H4><b><font color="dark blue">ADSL</font></b></H4>
					<P><b>ADSL (L2+) </b>- это модемная технология, преобразует
					стандартные абонентские телефонные
					аналоговые линии в линии высокоскоростного
					доступа. Канал – не симметричный,
					исходящая скорость от абонента – 1
					Мбит/с, входящая скорость к абоненту –
					до 15 Мбит/с.</P>
					<br>

				    <P><B>Основное преимущество данной технологии: </B>нет необходимости                        прокладывать кабель до
					абонента. Используются уже проложенные телефонные кабели, на которые
					устанавливаются сплиттеры для разделения сигнала на «телефонный» и                         «модемный».
					</P>
					<br>
					<P><B>Недостатки:</B> -ограничение	пропускной способности до 15                            Мбит/с по технологи ADSL, до 24 Мбит/с по технологии ADSL2+.</P>
					<br>
					<P>ADSL предполагает установку ADSL коммутатора со стороны узла, на                           котором происходит
					коммутация телефона и ADSL сигнала.</P>
					<P>Также, ADSL можно установить и без телефона, в
					данном случае к клиенту заводится телефонный кабель, который на                            прямую
					включается в модем, сплиттер при этом не используется. Телефонный                          сигнал в
					линию не подается.</P>
					<P>Подключение компьютеров к сети Интернет осуществляется
					по протоколу PPPoE (PPP over Ethernet Protocol) через
					сетевую карту, установленную в компьютер. Если такой карты нет, то                         есть возможность
					подключить модем к компьютеру при помощи USB порта.</P>
					
					<p class="label label-default pull-right">PPPoE - PPP over Ethernet                        Protocol</p>
